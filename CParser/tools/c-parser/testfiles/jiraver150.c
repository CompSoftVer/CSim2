unsigned f(unsigned x)
{
  return x + 1;
}

int f2(unsigned z)
{
  int y = z + 3;
  return y / 2;
}

unsigned g(unsigned y)
{
  return y + 2;
}

unsigned char h(unsigned char x)
{
  return x + 1;
}
